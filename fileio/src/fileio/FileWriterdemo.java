package fileio;

import java.io.FileWriter;
import java.io.IOException;

public class FileWriterdemo {

	public static void main(String[] args) throws IOException {
		FileWriter fw=new FileWriter("nitya.txt");
		fw.write(97);
		fw.write('\n');
		fw.write("khyati");
		char a[]= {'a','b','c'};
		fw.write('\n');
		fw.write(a);
		fw.flush();
		fw.close();
		
	}

}
